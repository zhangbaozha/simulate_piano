MYSUB  PROC                ;画线子程序

       CLC

       MOV BX,MYLENGTH

       MOV CX,X

       MOV DX,Y

       CMP MYFLAG ,1

       JE LINEV
       CMP MYFLAG ,2
       JE  LOP3          

       MOV AH,0CH

       MOV AL,7

LOP1:  INT 10H              ;画横线

       INC CX

       DEC BX

       JNE LOP1

       JMP SUBEND

LINEV: MOV AH,0CH

       MOV AL,7

LOP2:  INT 10H              ;画竖线

       INC DX

       DEC BX

       JNE LOP2
       JMP SUBEND
LOP3:                       ;画斜线


    mov ax,x2
    mov bx,x1
    cmp ax,bx
    jae dpos1
    sub bx,ax
    mov s1,-1
    mov xd,bx
    jmp d1
dpos1:
    sub ax,bx
    mov s1,1
    mov xd,ax   
d1: 
    mov ax,y2
    mov bx,y1
    cmp ax,bx
    jae dpos2    	;y2>=y1
    sub bx,ax    	;y2<y1
    mov s2,-1 
    mov yd,bx
    jmp d2
dpos2:
    sub ax,bx
    mov s2,1
    mov yd,ax
    
d2:  
    add ax,ax
    mov bx,xd 
    sub ax,bx  
    mov p,ax   		;2dy-dx
             
    mov cx,x1         
    mov dx,y1
    
pxie: 
 
    mov al,7    	;设置划线颜色为白色
    mov ah,0ch      ;写入点像
    int 10h
    
    mov ax,p
    mov bx,0
    cmp ax,bx
    jge ppos        ;p>=0
    jl pneg         ;p<0
                         
 
ppos:   
    mov ax,xd
    mov bx,yd
    cmp ax,bx
    ja ddpos1       ;xd>yd
    jbe ddneg1      ;xd<=yd
    
    
ddpos1: 		;0<k<1  或者  -1<k<0  同时  p>=0
    mov ax,x1		;x=x+1
    mov bx,s1
    add ax,s1
    mov x1,ax
    
    mov ax,s2		;y=y+1
    mov bx,y1
    add bx,ax
    mov y1,bx
    
    mov ax,p		;Pn+1=Pn+2(dy-dx)
    mov bx,xd
    mov cx,yd
    add bx,bx
    add cx,cx
    sub cx,bx
    add ax,cx
    mov p,ax
    
    jmp plot
 
ddneg1:			;k>1  或者  k<-1   同时  p>=0
    mov ax,y1 		;y=y+1
    inc ax
    mov y1,ax
    
    mov ax,s1		;x=x+1  或者  x=x-1
    mov bx,x1
    add bx,ax
    mov x1,bx
    
    mov ax,p		;Pn+1=Pn+2(dx-dy)
    mov bx,xd
    mov cx,yd
    add bx,bx
    add cx,cx
    sub bx,cx
    add ax,bx
    mov p,ax
    
    jmp plot
 
pneg:
    mov ax,xd
    mov bx,yd
    cmp ax,bx
    ja ddpos2       ;xd>yd
    jbe ddneg2      ;xd<=yd   
    
    
ddpos2:			;0<k<1  或者  -1<k<0  同时  p<0
    mov ax,x1		;x=x+1
    mov bx,s1
    add ax,s1
    mov x1,ax
    
    mov ax,p		;Pn+1=Pn+2dy
    mov bx,yd
    add bx,bx
    add ax,bx
    mov p,ax
    
    jmp plot
 
 
 
ddneg2: 		;k>1  或者  k<-1   同时  p<0
    mov ax,y1 		;y=y+1
    inc ax
    mov y1,ax
    
    mov ax,p		;Pn+1=Pn+2dx
    mov bx,xd
    add bx,bx
    add ax,bx
    mov p,ax
    
    jmp plot
    
 
plot:
    mov cx,x1		;X坐标
    mov dx,y1		;Y坐标
    cmp cx,x2		;是否绘图完毕
    jne pxie

  
SUBEND: RET

MYSUB  ENDP
